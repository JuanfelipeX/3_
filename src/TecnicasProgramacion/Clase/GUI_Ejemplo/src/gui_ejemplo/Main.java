    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        // TODO add your handling code here:
        if (nombreField.getText().isEmpty() || valorField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo nombre o valor no puede ser nulo");
        }else{
            String nombre = nombreField.getText();
            String valor = valorField.getText();
            Articulos articulo = new Articulos(nombre, valor, monedaActual);
            lista.add(articulo);

            nombreField.setText("");
            valorField.setText("");

            mostrarListaEnResultados();
        }
        
        
    }//GEN-LAST:event_guardarButtonActionPerformed

    private void mostrarListaEnResultados(){
        
        String impresion = "";
        for (int i=0; i<lista.size(); i++){
            impresion+="Nombre: "+lista.get(i).getNombre();
            impresion+=" Valor: "+lista.get(i).getValor();
            impresion+=" Moneda "+lista.get(i).getMoneda()+"\n";
        }
        
        resultadoField.setText(impresion);
    }
    
